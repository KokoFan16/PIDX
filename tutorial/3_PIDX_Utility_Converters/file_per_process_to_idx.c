#include <unistd.h>
#include <stdarg.h>
#include <stdint.h>

#include <getopt.h>
#include <string.h>
#include <assert.h>
#include <errno.h>
#include <stdlib.h>
#include <string.h>
#include <stdlib.h>

#if PIDX_HAVE_MPI
  #include <mpi.h>
#endif


/// main
int main(int argc, char **argv)
{

  int nprocs, rank;
#if PIDX_HAVE_MPI
  /// MPI initialization
  MPI_Init(&argc, &argv);
  MPI_Comm_size(MPI_COMM_WORLD, &nprocs);
  MPI_Comm_rank(MPI_COMM_WORLD, &rank);
#endif


#if PIDX_HAVE_MPI
  MPI_Finalize();
#endif
  return 0;
}
